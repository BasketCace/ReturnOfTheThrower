using Terraria;
using Terraria.ModLoader;
using Microsoft.Xna.Framework;

namespace ReturnOfTheThrower.Util
{
    internal class ProjectileUtil
    {
        public static NPC FindClosestNPC(float maxDetectDistance, Vector2 projCenter)
        {
            NPC closestNPC = null;

            float sqrMaxDetectDistance = maxDetectDistance * maxDetectDistance;

            for (int k = 0; k < Main.maxNPCs; k++)
            {
                NPC target = Main.npc[k];
                if (target.CanBeChasedBy())
                {
                    float sqrDistanceToTarget = Vector2.DistanceSquared(target.Center, projCenter);

                    if (sqrDistanceToTarget < sqrMaxDetectDistance)
                    {
                        sqrMaxDetectDistance = sqrDistanceToTarget;
                        closestNPC = target;
                    }
                }
            }

            return closestNPC;
        }
    }
}
